#include "../../../std_lib_facilities.h"
int main(void){
	int n;
	cin >> n;
	if(n == 0){
		cout << "zero" << endl;
	}else if(n == 1){
		cout << "one" << endl;
	}else if(n == 2){
		cout << "two" << endl;
	}else if(n == 3){
		cout << "three" << endl;
	}else if(n == 4){
		cout << "four" << endl;
	}else {
		cout << "Invalid Input";
	}

	return 0;
}
